def save_markdown_report(
    query: str, summary: str, articles: list, keywords: list, mode: str
) -> str:
    """Save research report as markdown file."""
    output_dir = Path.home() / "Documents" / "ACHEM"
    output_dir.mkdir(parents=True, exist_ok=True)

    safe_query = "".join(ch for ch in query if ch.isalnum() or ch in " -").replace(
        " ", "_"
    )[:50]
    filepath = output_dir / f"{safe_query}.md"

    existing = list(output_dir.glob(f"{safe_query}*.md"))
    if existing:
        existing[0].unlink()

    md_content = f"""# {query}

**Generated:** {datetime.now().strftime("%Y-%m-%d %H:%M:%S")}
**Sources:** {len(articles)}
**Mode:** {mode}

---

## Summary

{summary}

---

## Keywords

{", ".join(keywords[:10])}

---

## Source References

"""
    for i, article in enumerate(articles[:20], 1):
        title = article.get("title", "Unknown")
        url = article.get("url", "")
        relevance = article.get("relevance_score", 0)
        md_content += f"{i}. **{title}** (Relevance: {relevance}%)\n   {url}\n\n"

    md_content += f"""---

*Generated by ACHEM - Deep Web Research Tool*
"""

    filepath.write_text(md_content, encoding="utf-8")
    return str(filepath)


def detect_language(text: str) -> str:
    french_chars_set = set("àâäéèêëïîôùûüçÀÂÄÉÈÊËÏÎÔÙÛÜÇ")
    arabic_chars = sum(1 for ch in text if "\u0600" <= ch <= "\u06ff")
    french_chars = sum(1 for ch in text if ch in french_chars_set)
    total = len(text)
    if total == 0:
        return "en"
    if arabic_chars / total > 0.15:
        return "ar"
    if french_chars / total > 0.05:
        return "fr"
    return "en"


